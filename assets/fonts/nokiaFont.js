export class NokiaFont {
  constructor(pixelSize = 2) {
    this.pixelSize = pixelSize;
    this.charW = 5 * pixelSize + pixelSize; 
    this.charH = 7 * pixelSize;
  }

  setPixelSize(s) {
    this.pixelSize = s;
    this.charW = 5 * s + s;
    this.charH = 7 * s;
  }

  measureText(text) {
    return text.length * this.charW;
  }

  drawChar(ctx, ch, x, y, color) {
    const rows = G[ch] || G[' '];
    const ps = this.pixelSize;
    ctx.fillStyle = color;
    for (let row = 0; row < 7; row++) {
      const bits = rows[row];
      for (let col = 0; col < 5; col++) {
        if (bits & (1 << (4 - col))) {
          ctx.fillRect(x + col * ps, y + row * ps, ps, ps);
        }
      }
    }
  }

  drawText(ctx, text, x, y, color = '#FFFFFF', align = 'left') {
    const str = String(text).toUpperCase();
    const w = this.measureText(str);
    let ox = x;
    if (align === 'center') ox = x - w / 2;
    else if (align === 'right') ox = x - w;
    for (let i = 0; i < str.length; i++) {
      this.drawChar(ctx, str[i], Math.round(ox + i * this.charW), Math.round(y), color);
    }
  }

  drawTextShadow(ctx, text, x, y, color = '#FFFFFF', align = 'left') {
    this.drawText(ctx, text, x + 1, y + 1, '#000000', align);
    this.drawText(ctx, text, x, y, color, align);
  }
}
